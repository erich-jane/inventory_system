    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        String user = username.getText().trim();
        char[] passArr = password.getPassword();
        String pass = new String(passArr).trim();
        java.util.Arrays.fill(passArr, '\0');
        if(user.isEmpty() || pass.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Enter username and password","Missing credentials", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        config con = new config();
        String sql = "SELECT * FROM name WHERE email = ? AND password = ? AND status = ?";
        String role = con.authenticate(sql, username.getText(), password.getText(), "Active");
       
        if(role != null){
           JOptionPane.showMessageDialog(null, "LOGIN SUCCESSFUL!");
           if(role.equals("Admin")){
               dashboard ad = new dashboard();
               ad.setVisible(true);
               this.dispose();
           }else if(role.equals("User")){
               userDashboard ud = new userDashboard();
               ud.setVisible(true);
               this.dispose();
           }else{
               JOptionPane.showMessageDialog(null, "INVALID ACCOUNT!");
               return;
           }
           }else{
                JOptionPane.showMessageDialog(null, "INVALID CREDENTIALS / ACCOUNT INACTIVE");
        
        }
    }//GEN-LAST:event_loginMouseClicked

