    private String hash(String s){
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] b = md.digest(s.getBytes("UTF-8"));
            StringBuilder sb = new StringBuilder();
            for(byte by: b) sb.append(String.format("%02x", by));
            return sb.toString();
        } catch(Exception e){ return s; }
    }

    private void ensureUsersFileExists(){
        File f = new File("users.txt");
        if(!f.exists()){
            try{
                // create an empty users file; do not add default accounts
                java.io.File parent = f.getParentFile();
                if(parent != null && !parent.exists()) parent.mkdirs();
                f.createNewFile();
            } catch(IOException e){ e.printStackTrace(); }
        }
    }

    private String[] authenticateUser(String user, String pass){
        Map<String,String[]> users = readUsers();
        String h = hash(pass);
        if(users.containsKey(user) && users.get(user)[0].equals(h)){
            String role = users.get(user)[1];
            String fullname = (users.get(user).length>=3)?users.get(user)[2]:"";
            return new String[]{role, fullname};
        }
        return null;
    }

    private Map<String,String[]> readUsers(){
        Map<String,String[]> map = new HashMap<>();
        File f = new File("users.txt");
        if(!f.exists()) return map;
        try(BufferedReader br = new BufferedReader(new FileReader(f))){
            String line;
            while((line=br.readLine())!=null){
                String[] parts = line.split(":");
                if(parts.length>=2){
                    String uname = parts[0];
                    String phash = parts[1];
                    String role = (parts.length>=3)?parts[2]:"user";
                    String fullname = (parts.length>=4)?parts[3]:"";
                    map.put(uname, new String[]{phash,role,fullname});
                }
            }
        } catch(IOException e){ e.printStackTrace(); }
        return map;
    }

